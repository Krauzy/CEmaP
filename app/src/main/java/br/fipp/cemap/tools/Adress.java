package br.fipp.cemap.tools;

import android.content.Context;
import android.widget.Toast;

import org.json.JSONException;
import org.json.JSONObject;

import java.io.BufferedReader;
import java.io.InputStream;
import java.io.InputStreamReader;
import java.net.HttpURLConnection;
import java.net.URL;

public class Adress {
    private String cep;
    private String logradouro;
    private String complemento;
    private String bairro;
    private String localidade;
    private String uf;
    private String ibge;
    private String gia;
    private String ddd;
    private String siafi;

    public Adress(String JSON, Context context) {
        try {
            JSONObject reader = new JSONObject(JSON);
            this.cep = reader.get("cep").toString();
            this.logradouro = reader.get("logradouro").toString();
            this.complemento = reader.get("complemento").toString();
            this.bairro = reader.get("bairro").toString();
            this.localidade = reader.get("localidade").toString();
            this.uf = reader.get("uf").toString();
            this.ibge = reader.get("ibge").toString();
            this.gia = reader.get("gia").toString();
            this.ddd = reader.get("ddd").toString();
            this.siafi = reader.get("siafi").toString();
        }
        catch(JSONException ex) {
            Toast.makeText(context, ex.getMessage(), Toast.LENGTH_LONG);
        }
    }

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }

    public String getLogradouro() {
        return logradouro;
    }

    public void setLogradouro(String logradouro) {
        this.logradouro = logradouro;
    }

    public String getComplemento() {
        return complemento;
    }

    public void setComplemento(String complemento) {
        this.complemento = complemento;
    }

    public String getBairro() {
        return bairro;
    }

    public void setBairro(String bairro) {
        this.bairro = bairro;
    }

    public String getLocalidade() {
        return localidade;
    }

    public void setLocalidade(String localidade) {
        this.localidade = localidade;
    }

    public String getUf() {
        return uf;
    }

    public void setUf(String uf) {
        this.uf = uf;
    }

    public String getIbge() {
        return ibge;
    }

    public void setIbge(String ibge) {
        this.ibge = ibge;
    }

    public String getGia() {
        return gia;
    }

    public void setGia(String gia) {
        this.gia = gia;
    }

    public String getDdd() {
        return ddd;
    }

    public void setDdd(String ddd) {
        this.ddd = ddd;
    }

    public String getSiafi() {
        return siafi;
    }

    public void setSiafi(String siafi) {
        this.siafi = siafi;
    }
}
